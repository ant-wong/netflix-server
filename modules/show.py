class Movie:
    def __init__(self, id, imdbid, title, image, runtime, released, synopsis, type, date):
        self.id = id
        self.imdbid = imdbid
        self.title = title
        self.image = image
        self.runtime = runtime
        self.released = released
        self.synopsis = synopsis
        self.type = type
        self.date = date
